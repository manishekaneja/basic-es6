// Integer
var variable = 10;
console.log(variable);

// Float / Decimal
variable = 0.1;
console.log(variable);

// Strings / Decimal
variable = "string";
console.log(variable);

// Array
variable = [10, 20, 30];
console.log(variable);

// Object
variable = {
  a: -1,
  b: 1000,
};
console.log(variable);

// Function
variable = function () {
  console.log("I am a Function");
};
